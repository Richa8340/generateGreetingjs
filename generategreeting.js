// Function to generate a personalized greeting
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to our website.`;
}

// Using the function to greet three different people
const greeting1 = generateGreeting("Alice");
const greeting2 = generateGreeting("Bob");
const greeting3 = generateGreeting("Charlie");

// Displaying the greetings
console.log(greeting1); // Output: Hello, Alice! Welcome to our website.
console.log(greeting2); // Output: Hello, Bob! Welcome to our website.
console.log(greeting3); // Output: Hello, Charlie! Welcome to our website.
